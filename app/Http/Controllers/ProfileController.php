     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string|min:3|max:255',
            'username' => 'required|string|min:3|max:20|unique:users,username,' . Auth::user()->id,
            'email' => 'required|string|email:dns|max:255|unique:users,email,' . Auth::user()->id,
        ]);

        try
        {
            User::where('id', Auth::user()->id)->update($data);
        }
        catch(QueryException $e)
        {
            return redirect('/profile')->with('error', 'Terjadi kesalahan');
        }

        return redirect('/profile')->with('success', 'Profile berhasil diedit');
    }

